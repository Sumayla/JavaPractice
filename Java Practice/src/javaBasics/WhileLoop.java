package javaBasics;

public class WhileLoop {

	public static void main(String[] args) {


		String[] movies = {
				"troy",
				"marvel",
				"Avengers",
				"IronMan"
		};
		
		int i=0;
		while (i<movies.length)
			{
			System.out.println(movies[i]);
			i++;
			
			}
	

		
		
	}

}
